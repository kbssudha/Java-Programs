package day1;

public class Swapnumbers {
    public static void main(String[] args) {
        int a=5;
        int b=10;
        System.out.println("Before swapping : number-1 = "+a+"; number-2 = "+b);
        int c=a;
        a=b;
        b=c;
        System.out.println("After swapping : number-1 = "+a+ "; number-2 = "+b);
    }
}
